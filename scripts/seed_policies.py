def seed_policies():
    db = SessionLocal()
    try:
        # 清空旧数据
        db.query(PolicyDoc).delete()
        db.commit()
        print("已清空旧规则数据")

        # 插入新规则
        for rule in RULES:
            doc = PolicyDoc(
                title=rule["title"],
                content=rule["content"],
                category=rule["category"],
            )
            db.add(doc)
        db.commit()
        print(f"成功写入 {len(RULES)} 条图书馆规则")

        # 验证
        count = db.query(PolicyDoc).count()
        print(f"数据库中规则总数: {count}")

        # 重建 ChromaDB 索引
        print("\n重建 ChromaDB 规则索引...")
        pipeline = IngestionPipeline()
        result = pipeline.build_policy_index()
        print(f"ChromaDB 索引构建完成")
    finally:
        db.close()


if __name__ == "__main__":
    seed_policies()
